const sum = (a,b) => {
    return a+b;
}

const sub = (a,b) => {
    return a-b;
}

const prod = (a,b) => {
    return a*b;
}

const div = (a,b) => {
    return a/b;
}

const sin = (a) => {
    return Math.sin(a);
}

const cos = (b) => {
    return Math.cos(b);
}

const log = (a) => {
    return Math.log2(a);
}

module.exports = {sum,sub,prod,div,sin,cos,log};
